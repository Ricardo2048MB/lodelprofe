SEND_CRLF PROC
	PUSH AX
	PUSH DX
	MOV AH,2
	MOV DL,CR
	INT 21H
	MOV DL,LF
	INT 21H
	POP DX
	POP AX
	RET
SEND_CRLF ENDP



