.MODEL SMALL

.STACK

.DATA

VECTOR	DB	10H, 11H, 12H, 13H, 14H, 15H, 16H, 17H
		DB	18H, 19H, 1AH, 1BH,	1CH, 1DH, 1EH, 1FH

.CODE

	EXTRN	WRITE_HEX:PROC
	EXTRN	wRITE_CHAR:PROC
DISP_LINE	PROC
	MOV	AX,@DATA
	MOV	DS,AX

	XOR	BX,BX
	MOV	CX,16
HEX_LOOP:
	MOV	DL,VECTOR[BX]
	CALL WRITE_HEX
	MOV	DL,' '
	CALL WRITE_CHAR
	INC BX
	LOOP HEX_LOOP

	MOV DL,' '
	CALL WRITE_CHAR
	MOV CX,16
	XOR BX,BX
ASCII_LOOP:
	MOV DL,VECTOR[BX]
	CALL WRITE_CHAR
	INC BX
	LOOP ASCII_LOOP
	
	.EXIT
DISP_LINE ENDP
	END DISP_LINE
